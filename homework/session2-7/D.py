import sys

a=[".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.", "---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."]

s=sys.stdin.read().strip().upper()

for c in s:
    if ord('A')<=ord(c)<=ord('Z'):
        print(a[ord(c)-ord('A')],end='',sep='')
    else:
        print(c,end='',sep='')
print()